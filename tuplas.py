# Criando primeira tupla
primeira_tupla = (1, 2, 3, 4, "Olá, tupla")

# Imprimindo tupla
print("Conteúdo da tupla:", primeira_tupla)

# Usando 'index' para encontrar o índice do elemento 4
indice_quatro = primeira_tupla.index(4)
print("Índice do elemento 4:", indice_quatro)

# Verificando se a tupla contém o elemento 3
contém_tres = 3 in primeira_tupla
print("A tupla contém o elemento 3?", contém_tres)

# Verificando se a tupla contém o elemento 33
contém_trinta_tres = 33 in primeira_tupla
print("A tupla contém o elemento 33?", contém_trinta_tres)